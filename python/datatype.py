# Numeric int(int is updated in long in python3)
a = 10
print(a)
print(type)
print(type(a))

# Numeric float
b = 10.2
print(b)
print(type(b))

# Numeric complex
c = 10+2j
print(c)
print(type(c))

# NoneType
d = None
print(d)
print(type(d))

# bool
e = True
print(e)
print(type(e))

# Binary bytes
f = b"Hello"
print(f)
print(type(f))	

# Binary bytearray
g = bytearray(5)	
print(g)
print(type(g))

# Binary memoryview
h = memoryview(bytes(5))
print(h)
print(type(h))

# sequence list Mutable
l = [1,2,3,3,2,1]
print(l)
print(type(l))

# sequence tuple
t = (1,2,3,3,2,1)
print(t)
print(type(t))

# sequence range
r = range(6)
print(r)
print(type(r))

# settype set mutable
s = {1,2,3,3,2,1}
print(s)
print(type(s))

# settype frozenset
fs = frozenset({"apple", "banana", "cherry"})
print(fs)
print(type(fs))

# mapping type dict mutable
d = {"apple":'seb', "banana":'kela', "cherry":'cheri'}
print(d)
print(type(d))

# text type str
s = "vidhan"
print(s)
print(type(s))
