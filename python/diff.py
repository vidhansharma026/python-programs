# list mutable, ordered, duplicate
l = [1,2,3,3,2,1]
# l = [] for empty
print(l)
print(type(l))

# tuple immutable, ordered, duplicate
t = (1,2,3,3,2,1)
# t = () for empty
print(t)
print(type(t))

# set mutable, unordered, non duplicate
s = {1,2,3,3,2,1}
# s = set({1,2,3,3,2,1})
# s = set() #for empty
print(s)
print(type(s))

# dict mutable, unordered, non duplicate key but value can
d = {'key':'value','name':'vidhan','rollno':176}
# l = {} for empty
print(d)
print(type(d))